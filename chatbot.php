    public function cleanText($text) {
        $text = mb_strtolower($text, 'UTF-8');
        $text = preg_replace('/[؟?,.!]$/u', '', $text);
        $text = preg_replace('/\s+/', ' ', $text);
        return trim($text);
    }
    
    public function extractHeirs($text) {
        $heirs = [];
        $patterns = [
            "زوجة" => '/(زوجة|زوجتي)/u',
            "زوج" => '/(زوج\s|الزوج\s|الزوج$)/u',
            "ام" => '/(أم|ام\s|الأم)/u',
            "اب" => '/(أب|اب\s|الأب)/u',
            "ابن" => '/(ابن|الابن|ابناء)/u',
            "بنت" => '/(بنت|البنت|ابنة)/u',
            "اخت" => '/(أخت|اخت\s|الأخت)/u',
            "اخ" => '/(أخ|اخ\s|الأخ)/u'
        ];
        
        foreach ($patterns as $heir => $pattern) {
            if (preg_match($pattern, $text)) {
                $heirs[] = $heir;
            }
        }
        
        return array_unique($heirs);
    }
    
