        if (count($heirs) > 1) {
            $calculation = $this->calculateInheritance($heirs);
            if ($calculation) {
                return $calculation . "\n\n📖 المرجع: سورة النساء";
            }
        }
        
        // Recherche dans la base de connaissances
        $bestMatch = null;
        $maxScore = 0;
        
        foreach ($this->knowledgeBase as $item) {
            foreach ($item["keywords"] as $keyword) {
                if (strpos($cleanQuestion, $keyword) !== false) {
                    $score = strlen($keyword);
                    if ($score > $maxScore) {
                        $maxScore = $score;
                        $bestMatch = $item;
                    }
                }
            }
        }
        
        if ($bestMatch) {
            return $bestMatch["answer"] . "\n\n📖 المرجع: " . $bestMatch["source"];
